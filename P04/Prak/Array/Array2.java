public class Array2{
    public static void main(String args[]){
        // Deklarasi Array
        String[] mahasiswa = {"Aldi", "Bintang", "Dina", "Zahwa"};

        // Menampilkan isi array pada index ke-2
        for(int i=0;i<mahasiswa.length;i++){
            System.out.println(mahasiswa[i]);
        }
    }
}